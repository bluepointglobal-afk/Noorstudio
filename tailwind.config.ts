// RTL support plugin
function rtlPlugin({ addUtilities }: any) {
  addUtilities({
    // Text direction utilities
    "[dir='rtl']": {
      direction: "rtl",
    },
    "[dir='ltr']": {
      direction: "ltr",
    },
    // Flip horizontal margins/padding for RTL
    "[dir='rtl'] .mr-auto": {
      marginRight: "auto",
      marginLeft: "0",
    },
    "[dir='rtl'] .ml-auto": {
      marginLeft: "auto",
      marginRight: "0",
    },
    "[dir='rtl'] .text-right": {
      textAlign: "left",
    },
    "[dir='rtl'] .text-left": {
      textAlign: "right",
    },
  });
}

